    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Botão salvar ediçao
        int i;
        ArrayList<Cliente> todosClientes = new ArrayList();
        Cliente updatedCliente;
        for (i = 0; i < table.getModel().getRowCount(); i++ ){
          //System.out.println(table.getModel().getValueAt(i,0) + " " + table.getModel().getValueAt(i,1));
            updatedCliente = new Cliente((int)i, (String)table.getModel().getValueAt(i,0).toString(), (String)table.getModel().getValueAt(i,1).toString());
            
            todosClientes.add(updatedCliente);
            System.out.println("Tabela: " + table.getModel().getValueAt(i,0).toString() + " - " + table.getModel().getValueAt(i,1).toString() + " Lista :" +  todosClientes.get(i).getId()+ " - " + todosClientes.get(i).getNome() + " - " + todosClientes.get(i).getCpf()  );
        }
        ClienteController aux = new ClienteController();
        aux.updateClientes(todosClientes);
        redrawTable();
        
    }//GEN-LAST:event_jButton4ActionPerformed

