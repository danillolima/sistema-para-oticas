    private void redrawTableUsuario() {
        UsuarioController usuarioController = new UsuarioController();
        ArrayList<Usuario> todosUsuarios = usuarioController.leUsuarios();
        jPanel9.removeAll();
        
        tableModel = new DefaultTableModel(columnNamesUsuario, 0);
       
        //tableModel.addTableModelListener(new MyTableModelListeners());
 
        table = new JTable(tableModel);
    
        for(int i = 0; i< todosUsuarios.size(); i++){
            String usuario = todosUsuarios.get(i).getUsuario();
            //cargo
            String nome = todosUsuarios.get(i).getNome();
            String cpf = todosUsuarios.get(i).getCpf();
            String cargo;
            if(todosUsuarios.get(i).getCargo() == 1){
                cargo = "Administrador";
            }
            else{
                cargo = "Funcionario";
            }
            System.out.println(usuario + cargo + nome + cpf );
            Object[] info = {usuario, cargo, nome, cpf};
            tableModel.addRow(info);
        }
        
        JScrollPane scrollPane = new JScrollPane(table);
        table.setFillsViewportHeight(true);
        jPanel9.add(scrollPane);
        jPanel9.repaint();
        jPanel9.revalidate();

    }


